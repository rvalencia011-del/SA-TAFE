def extract_frequent_assets(portfolios, threshold=9):
    """
    Analiza los portafolios resultantes de múltiples ejecuciones y extrae
    aquellos activos que aparecen con un peso mayor a cero en al menos 'threshold' ocasiones.

    Analyze the portfolios resulting from multiple runs and extract
    those assets that appear with a weight greater than zero on at least ‘threshold’ occasions.
    """
    asset_counts = {}

    # Recorrer cada ejecución (cada 'portfolio_final')
    for p in portfolios:
        # El DataFrame de pesos es el elemento índice 9 del retorno de SAIPO
        portfolio_df = p[9]

        # Filtrar activos con peso significativo (mayor a 0.005 para evitar ruido)
        active_assets = portfolio_df[portfolio_df['Weight'] > 0.005].index

        for asset in active_assets:
            asset_counts[asset] = asset_counts.get(asset, 0) + 1

    # Filtrar por el umbral solicitado
    frequent_assets = {asset: count for asset, count in asset_counts.items() if count >= threshold}

    # Crear un DataFrame para mejor visualización
    df_frequent = pd.DataFrame.from_dict(frequent_assets, orient='index', columns=['Frecuencia'])
    df_frequent = df_frequent.sort_values(by='Frecuencia', ascending=False)

    return df_frequent

