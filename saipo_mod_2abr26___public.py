def sigma(w, C): # Portfolio's variance
    return np.dot(w,np.dot(C,w.T))

def portfolio_risk(w, C):
    """
    Calcula la volatilidad del portafolio.
    Asegura que w y C tengan dimensiones compatibles para un escalar.

    Calculate the portfolio volatility.
    Ensure that w and C have dimensions compatible with a scalar
    """
    w = np.asarray(w).flatten()
    C = np.asarray(C)

    # Cálculo matricial w^T * C * w
    variance = np.dot(w, np.dot(C, w))

    # np.maximum evita problemas con números negativos infinitesimales por precisión
    return np.sqrt(np.maximum(0, variance)).item()*period**(1/2)



def Sharpe_port(return_portfolio, rf, risk_portfolio): # Sharpe with correlation
    return (return_portfolio - rf) / risk_portfolio

def Sharpe(return_portfolio, rf, sigma): # Classic Sharpe
    return (return_portfolio - rf) / (sigma**(1/2)*period**(1/2))

def return_port(ves, w):
    """Calcula el rendimiento esperado como escalar.
    Calculate the expected return as a scalar
    """
    ves = np.asarray(ves).flatten()
    w = np.asarray(w).flatten()
    return np.dot(ves, w).item()

def corr_prom(w, desv_std, p_risk):
    """
    Calcula la correlación promedio ponderada del portafolio.
    Optimizado para evitar ciclos anidados.
    Calculate the weighted average correlation of the portfolio.
    Optimized to avoid nested loops.
    """
    # Calculamos la varianza individual ponderada: sum(w_i^2 * sigma_i^2)
    weighted_var_individual = np.sum((w**2) * (desv_std**2))

    # La varianza total del portafolio es: V_indiv + V_cross
    # Por lo tanto, la parte de covarianza cruzada es:
    cross_term = (p_risk**2) - weighted_var_individual

    # Calculamos el denominador para promediar: sum_{i != j} (w_i * w_j * sigma_i * sigma_j)
    # Esto se obtiene del cuadrado de la suma de (w * sigma) menos la suma de los cuadrados
    sum_w_std = np.sum(w * desv_std)
    denominator = (sum_w_std**2) - weighted_var_individual

    # Evitar división por cero si el portafolio está concentrado en un solo activo
    if denominator == 0: return 0
    return cross_term / denominator

def RegularPerturbation(w, q):
    """
    Perturbación: transfiere peso de un activo a otro y normaliza.
    Disturbance: transfers weight from one asset to another and normalizes
    """
    w_new = w.copy()
    idx1, idx2 = random.sample(range(len(w_new)), 2)

    # Lógica de transferencia corregida
    transfer = min(q, w_new[idx1])
    w_new[idx1] -= transfer
    w_new[idx2] += transfer

    # Normalización de seguridad para mantener sum(w) == 1.0
    return w_new / np.sum(w_new)

